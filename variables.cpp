#include<iostream>
using namespace std;
int main(){
    // inter data type
    int apples = 5;
    cout<<apples<<endl;
    cout<<"size of int:"<<sizeof(apples)<<endl;
    
    //char data type
    char sign = '&';
    cout<<"size of char:"<<sizeof(sign)<<endl;
    
    //boolean data type
    bool flag = true;
    cout<<flag<<endl;
    cout<<"size of bool:"<<sizeof(flag)<<endl;
    
    //float data type
    float score = 3.14;
    cout<<score<<endl;
    cout<<"size of float:"<<sizeof(score)<<endl;
}